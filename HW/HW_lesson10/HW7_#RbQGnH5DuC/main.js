function addToLocalStorage(arrayName, objToAdd) {
    arrayGasket = JSON.parse(localStorage.getItem(arrayName)) || [];
    if(typeof objToAdd===`object`){
        arrayGasket[arrayGasket.length] = objToAdd;
        localStorage.setItem(arrayName, JSON.stringify(arrayGasket));
    }
}

